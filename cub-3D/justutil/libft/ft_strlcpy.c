#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t	i;
	size_t	b;

	i = 0;
	b = 0;
	while (src[b])
		b++;
	if (dstsize == 0)
		return (b);
	while (i < dstsize - 1 && src[i])
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (b);
}
